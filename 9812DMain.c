void encoderforwards (float howMany)
{
	SensorValue[encoder] = 0;
	while(SensorValue[encoder] < howMany*90)
	{
		motor[right] = 127;
		motor[left] = 127;
	}
	motor[right] = 0;
	motor[left] = 0;

}

void encoderright (float howMany)
{
	SensorValue[encoder] = 0;
	while(SensorValue[encoder] < howMany*90)
	{
		motor[right] = -127;
		motor[left] = 127;
	}
	motor[right] = 0;
	motor[left] = 0;

}
void encoderleft (float howMany)
{
	SensorValue[encoder] = 0;
	while(SensorValue[encoder] < howMany*90)
	{
		motor[right] = 127;
		motor[left] = -127;
	}
	motor[right] = 0;
	motor[left] = 0;

}
void encoderbackward (float howMany)
{
	SensorValue[encoder] = 0;
	while(SensorValue[encoder] < howMany*90)
	{
		motor[right] = -127;
		motor[left] = -127;
	}
	motor[right] = 0;
	motor[left] = 0;

}

